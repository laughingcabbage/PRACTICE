SELECT l_code AS 대출종목코드
	  ,l_store AS 대출지점
	  ,l_date AS 대출일자
	  ,l_qty AS 대출건수
	  ,l_total AS 대출액
	  ,SUM(l_total) OVER(PARTITION BY l_code, l_store ORDER BY l_date) AS 누적대출금액
FROM loan
ORDER BY l_code, l_store, l_date;

